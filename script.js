// 층 표시 및 강의실 상태 갱신 함수
function showFloor(floor) {
  const now = new Date();

  const dayMap = {
    "Monday": "월",
    "Tuesday": "화",
    "Wednesday": "수",
    "Thursday": "목",
    "Friday": "금",
    "Saturday": "토",
    "Sunday": "일"
  };
  const weekdayEng = now.toLocaleDateString('en-US', { weekday: 'long' });
  const weekday = dayMap[weekdayEng];

  const nowSlot = getCurrentTimeSlot();
  const h = String(now.getHours()).padStart(2, '0');
  const m = String(now.getMinutes()).padStart(2, '0');
  document.getElementById("current-time").textContent = `현재시간 : ${h}:${m} (${weekday})`;

  document.querySelectorAll(".floor-button").forEach(btn => btn.classList.remove("active"));
  document.querySelector(`.floor-button[onclick="showFloor('${floor}')"]`)?.classList.add("active");

  const container = document.getElementById("room-container");
  container.innerHTML = "";

  const daySchedule = weeklySchedule[weekday] || {};
  const rooms = floorRooms[floor] || [];

  rooms.forEach(room => {
    const box = document.createElement("div");
    box.classList.add("room-box");

    const roomTimes = (daySchedule[room] || []).map(t => t.toUpperCase());
    const isOccupied = roomTimes.includes(nowSlot.toUpperCase());

    box.classList.add(isOccupied ? "occupied" : "available");
    box.textContent = room;
    container.appendChild(box);
  });
}

// 매 1분마다 현재 시간 슬롯이 바뀌었는지 확인 후 자동 새로고침
let lastTimeSlot = getCurrentTimeSlot();

setInterval(() => {
  const currentSlot = getCurrentTimeSlot();
  if (currentSlot !== lastTimeSlot) {
    lastTimeSlot = currentSlot;
    const selectedFloor = document.querySelector(".floor-button.active")?.textContent;
    if (selectedFloor) {
      showFloor(selectedFloor);
    }
  }
}, 60 * 1000);
