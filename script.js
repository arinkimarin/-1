function getCurrentTimeSlot() {
  const now = new Date();
  let hour = now.getHours();
  let minute = now.getMinutes();
  const suffix = hour >= 12 ? "PM" : "AM";
  if (hour > 12) hour -= 12;
  if (hour === 0) hour = 12;
  return `${hour}:${minute < 30 ? "00" : "30"}${suffix}`;
}

